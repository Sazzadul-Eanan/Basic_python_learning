# elif statement-
# remember in 'elif' statement, if any of the conditions works, then rest of the conditions will not be checked by the program
marks = 70

if marks >= 80 :
    print('A+')
elif marks >= 70 :
    print('A')
elif marks >= 60 :
    print('A-')
elif marks >= 50 :
    print('B')

else:
    print('Fail')