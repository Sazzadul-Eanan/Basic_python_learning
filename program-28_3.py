# Basic difference between 'for' & 'while' loop


i = 3                 # here i is a variable

while i <= 5 :        # in a 'while loop' the iteration starts from the value as it is declared in the 'initialization' such as 'i = 2' and increase 1 by 1, up to the highest value
    print(i)          # in 'while loop' number of iterations is unknown but, the conditions for stopping is known
    i = i + 1


