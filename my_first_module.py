
# A function for calculating the area of a triangle

def triangle_area (b , h) :                              # 'b' for base    # 'h' for height
    area = 0.5 * b * h
    print ('Area of the triangle is : ', area)




# A function for calculating the area of a rectangle

def rectangle_area (l , w) :                              # 'l' for length    # 'h' for height
    area = l * w
    print ('Area of the rectangle is : ', area)